<h2 style="text-align: center;">Job Seeking Skills</h2>

<p>Finding a job is a job within itself! A new career is not going to come to you without effort. An effective job search requires that you actively pursue leads from a variety of sources.</p>

<p>Each day, make it your goal to accomplish at least one of the following action items:</p>

<img src="/images/interview-01.png" alt="" style="float: right;" />

<ol class="bullets bold red">

  <li>
  <div>Change your voice mail message.</div>
  <div class="textdefault">Before your resume goes anywhere, make sure that your voice mail message sounds professional. Simply say &quot;This is Jane Smith. I am unable to take your call right now. Please leave your name, telephone number and a brief message and I will get back to you as soon as possible.&quot; Do not use music of any kind on your voice mail.</div>
  </li>

  <li>
  <div>Create a professional email account.</div>
  <div class="textdefault">Many job postings request that you email your resume or application. Use complete sentences and check your spelling email, and proper spacing. Make sure that your email address is professional. Some combination of your first and last name is best, for example asjsmth@yahoo.com or jane.smith@msn.com. If you do not have an email address, you may set up a free email account at www.yahoo.com or www.msn.com.</div>
  </li>

  <li>
  <div>Find at least one job posting every day.</div>
  <div class="textdefault">Set aside an hour daily and try to find one job to apply for every day. Use the library or some other quiet place where you won't be distracted. Look online, check the newspaper, call places that you want to work and ask if they're hiring. Use the list of websites below to help you in your job search.</div>
  </li>

  <li>
  <div>Apply, apply, apply and APPLY MORE OFTEN!</div>
  <div class="textdefault">Make sure that you are applying correctly! Most job advertisements tell you if they want a resume and cover letter, salary history or application. If you have questions, or if you're extremely interested in a position, call the company and ask who the Hiring Manager is, and see if you can talk to them about applying for the position. Sometimes they'll be flexible on one of the job requirements, equal amount of experience or foreign language requirements or they'll have a unadvertised position they need to fill. The best way to find out is to call.</div>
  </li>
</ol>
