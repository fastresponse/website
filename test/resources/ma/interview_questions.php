<h2 style="text-align: center;">Interview Questions - Medical Assistant</h2>

<h3>General Questions</h3>
<ol>
  <li>Tell me about yourself.</li>
  <li>What do you know about our organization?</li>
  <li>What is an example of one of your accomplishments?</li>
  <li>Tell me about your transition from your previous career to working as a medical assistant.</li>
  <li>Give me an example of a disagreeable patient you have had to deal with?</li>
  <li>Tell me about a time when you handled multiple tasks at once?</li>
  <li>What are the three most important traits a person needs to have for this job? Rate yourself on these qualities.</li>
  <li>How do you handle stress on the job?</li>
  <li>What computer programs or electronic health records programs are you acquainted with?</li>
  <li>What types of injections are you capable to give?</li>
  <li>How would you describe your communication skills?</li>
  <li>Why do you want this job position?</li>
  <li>What career or educational goals do you have?</li>
  <li>What is your greatest strength/weakness?</li>
  <li>How soon are you available to starting working?</li>
  <li>Are you willing to relocate in the future?</li>
  <li>Do you have any planned vacations in the coming months where you would need days off?</li>
  <li>What hourly pay rate are you looking for?</li>
</ol>

<h3>Scenario Based Questions</h3>
<ol>
  <li>While working in the clinic, a Physician comes up and asks you to perform a task that you know is absolutely out of your scope of practice. </li>
  <li>While retrieving some OTC medications from the storage area, you notice another Medical Assistant putting medication samples in his back pack. You ask him what he is doing and he responds with, "It's ok, Dr. Johnson said I can take some samples home to my family".</li>
</ol>

<h3>Applicant Questions for Job Interviews</h3>
<ol>
  <li>What do you enjoy most about working here?</li>
  <li>What opportunities for advancement are available within the organization?</li>
  <li>Can I provide you with any further information?</li>
  <li>When do you expect to make your final decision?</li>
</ol>

<h3>Ending a Job Interview</h3>
<ol>
  <li>Thank the interviewer for their time and tell them that you would like the job.</li>
  <li>Offer to shake the interview's hand.</li>
  <li>Have the interviewer's name, correct spelling, and email address to send a thank you email.</li>
</ol>
