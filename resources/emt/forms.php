  <li class="bold underline yellow">Ambulance Driver Certificate Information</li>
  <ul class="bullets">
    <li>Here is the <a href="https://www.dmv.ca.gov/dl/ambu_drvr_cert.htm" class="yellow">DMV's FAQ page</a>.  This link describes all the steps needed to obtain a California Ambulance Driver Certificate.</li>
    <li>Here is the <a href="http://apps.dmv.ca.gov/forms/dl/dl51.pdf" class="yellow">direct link to the form DL-51</a>.</li>
    <li>The form can be filled out by an MD, DO, PA, NP, or DC.  The examiner <strong>must be listed on the National Registry of Certified Medical Examiners</strong>.</li>

    <li>Changes to the Medical Examiner Certificate</li>
    <ul class="bullets" style="margin-bottom: 0;">
      <li>Effective January 1st, 2014, form DL-51A / Medical Examiner Certificate (the "Green Card") is obsolete. In lieu of this form, drivers must use the <a href="http://www.fmcsa.dot.gov/documents/safetyprograms/Medical-Examiners-Certificate.pdf" class="yellow">Federal Medical Examiner's Certificate</a>. You can bring in a printed copy, or the Medical Secretary at your physician's office may also have a copy. However, you should confirm that before your visit.</li>
      <li>If you already have a valid DL-51A Green Card, then that card is still valid until its expiration date.</li>
      <li><a href="http://apps.dmv.ca.gov/pubs/olin/14_olin/14olin02.pdf" class="yellow">This document</a> explains the changes.</li>
    </ul>

    <li>What we require for externships</li>
    <ul class="bullets" style="margin-bottom: 0;">
      <li>We do NOT require the DL-51 to be completed in order to schedule externships. However, <strong>we highly recommend it</strong>, especially for students who will be seeking ambulance work.</li>
      <li>What is required is a note from an MD, DO, PA, NP, DC, or RN stating that they have examined you and have found you physically fit for work in an emergency room in the capacity of an ED Technician, and that your vision is not seriously impaired (correctable to at least 20/40 vision and able to differentiate between standard traffic signal colors).</li>
      <li>A photocopy of the completed DL-51, DL-51A ("Green Card"), the Federal Medical Examiner's Certificate, or a handwritten or typed note signed by the above medical professionals are all acceptable and sufficient to meet our externship requirements.</li>
    </ul>
