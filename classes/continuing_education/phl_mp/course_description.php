<section id="course_description">
<p>The Phlebotomy for Medical Professionals program is a one-day, 6-hour course designed for all those who hold a State of California Phlebotomy license or are medical professionals (LVNs, RNs, Physicians, MAs and EMTs) and would like to make themselves more valuable in the workplace and/or augment or polish their skills. This program provides both classroom and practical (skills) instruction.</p>
<p>Lectures will cover:
<ul>
  <li>Application of basic concepts of blood equipment and blood collection tubes.</li>
  <li>Additives and order of draw.</li>
  <li>Variables as sources of error in specimen handling.</li>
  <li>OSHA, appropriate disposal of sharps, and other hazardous materials.</li>
  <li>Patient ID, specimen ID, post-procedure care and specimen handling.</li>
</ul>
</p>
<p>Practical Instruction provides:
<ul>
  <li>Hands-on training on the methods of fingersticks and venipuncture collection using butterflies and ETSs.</li>
  <li>Students will perform 5-10 venipunctures during the program. The students work in teams, drawing on one another.</li>
</ul>
</p>
<p>Where can I work with this certification?
<ul>
  <li>You will be qualified to work in physician's office.</li>
  <li>You will not be qualified to work in a clinical laboratory or a public agency.</li>
  <li>Students seeking to work in a public health clinic or lab should consider enrolling in our Phlebotomy Technician (CPT-1) program.</li>
</ul>
</p>
