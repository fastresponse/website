	    <h4>ECG Monitor Technician:</h4>
	    <p>Our ECG Technician Program includes Medical Terminology, Anatomy and Physiology of the cardiovascular system, cardiac conduction system, scope of practice, and diagnostic testing for cardiovascular related disorders and diseases. Students will gain the skills needed to operate and interpret the 3-Lead, 5-Lead, and 12-Lead ECG. Students will also be thoroughly trained in obtaining and recording blood pressure, respirations, pulse, Oxygen Saturation (SPO<sub>2</sub>), and temperature.</p>
