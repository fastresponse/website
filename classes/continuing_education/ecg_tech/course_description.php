<p>Our ECG Technician Program includes Medical Terminology, Anatomy and Physiology of the cardiovascular system, cardiac conduction system, scope of practice, and diagnostic testing for cardiovascular related disorders and diseases. Students will gain the skills needed to operate and interpret the 3-Lead, 5-Lead, and 12-Lead ECG. Students will also be thoroughly trained in obtaining and recording blood pressure, respirations, and pulse.</p>

<p>Rhythm recognition discussed throughout the program consists of, but is not limited to the following:</p>

<ul>
	<li>Sinus Rhythms</li>
	<li>Atrial Rhythms</li>
	<li>Ventricular Rhythms</li>
	<li>Junctional Rhythms</li>
  <li>Heart Blocks</li>
	<li>Myocardial Infarction</li>
	<li>Artifact and ECG Troubleshooting</li>
</ul>

<p>Upon successful completion of the program, students will be awarded a Certificate of Completion. The certificate allows the student to take the National Center for Competency Testing (NCCT) ECG Technician Exam. Upon successful completion of the NCCT exam, students will be Nationally Licensed ECG Technicians.</p>
