      <h1>Anatomy and Physiology</h1>
    </div>
  </div>
  <div class="body">
    <p>
    The purpose of the Anatomy and Physiology (A&amp;P) prerequisite course is to deliver a comprehensive theoretical foundation and medical terminology of the human body systems to the paramedic program candidate comparable to what they will encounter during their didacticportion of the Paramedic Academy.  Successful completion of this prerequisite will raise the candidate's  cognitive level to that of a better prepared beginning paramedic student, and will verify the candidate's ability to cope with the pace of the School's Paramedic Academy. All Paramedic Academy candidates who have not completed a Collegiate Level A&amp;P course must complete this course.  
    </p>

    <p>
    Sections of this course may be held in an online format.  All students must have online access and the ability to complete assignments autonomously. Intermittent quizzes will be given as formative assessments to ensure that each student is advancing sufficiently in the course.  
    </p>

    <p>
    Schedule: Monday / Tuesday / Wednesday from 9:00 A.M. to 6:00 P.M.
    </p>

    <p>
    Cost: $450.00 - Books included.
    </p>

    <p><a href="http://www.ssreg.com/fastresponse/classes/classes.asp?catID=4216">Please register for Fast Response School's Anatomy &amp; Physiology course online.</a></p>

    <hr />

    <p>
    Applicants who have completed a college level Anatomy and Physiology course within the last 2 years may forgo the Anatomy and Physiology course by submitting college transcripts to Fast Response; however it is still strongly suggested that students take our Anatomy and Physiology class to be prepared for the Paramedic Academy.  
    </p>
